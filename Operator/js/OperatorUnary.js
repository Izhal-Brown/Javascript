/*
C. Operator Unary :
  +  --> Menandakan nilai Positif
  -  --> Menandakan nilai Negatif
  ++ --> Increment, Menaikkan 1 angka
  -- --> Decrement, Menurunkan 1 angka
*/

let resultU = +1; // positif 1
document.writeln("<p>" + resultU + "</p>")

resultU--
document.writeln("<p>" + resultU + "</p>")

resultU++
document.writeln("<p>" + resultU + "</p>")

resultU = -resultU // negatif
document.writeln("<p>" + resultU + "</p>")

